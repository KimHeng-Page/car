package java3;
public class switchcase {
    public static void main(String[] args){
        int num = 1;
        switch(num){
            case 1:
                System.out.print("Monday");
                break;
            case 2:
                System.out.print("Tuesday");
            break;
            default:
                System.out.println("fail");
                break;
        }
    }
}

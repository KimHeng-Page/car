package java3;
public class index5 {
    public static void main(String[] args) {
        System.out.println("   1 x 1 = 1          2 x 1 = 2");
        for (int i = 1; i <= 10; i++) {
            System.out.print("   1 x " + i + " = " + (1 * i));
            System.out.print("        "); 
            System.out.print("  2 x " + i + " = " + (2 * i));
            
            System.out.println();
        }
    }
} 
